"""This is a template for Auto-GPT plugins."""
from typing import Any, Dict, List, Optional, Tuple, TypeVar, TypedDict
import os

from auto_gpt_plugin_template import AutoGPTPluginTemplate

PromptGenerator = TypeVar("PromptGenerator")


class Message(TypedDict):
    role: str
    content: str


class AutoGPT_YouTube(AutoGPTPluginTemplate):
    """
    This is a plugin for Auto-GPT which enables access to certain YouTube features.
    """

    def __init__(self):
        super().__init__()
        self._name = "AutoGPT-YouTube"
        self._version = "0.2.0"
        self._description = "This is a plugin for Auto-GPT which enables access to certain YouTube features."

        # Get the YouTube API key from the env. if it does not exist give a warning
        self.yt_api_key = os.environ.get("YOUTUBE_API_KEY")
        if self.yt_api_key is None:
            print(
                "WARNING: The YouTube API key is not set, therefore API commands are disabled. Please set the YOUTUBE_API_KEY=your_api_key environment variable."
            )
        
        self.workspace_path = "auto_gpt_workspace"
        

    def can_handle_post_prompt(self) -> bool:
        """This method is called to check that the plugin can
        handle the post_prompt method.

        Returns:
            bool: True if the plugin can handle the post_prompt method."""
        return True

    def post_prompt(self, prompt: PromptGenerator) -> PromptGenerator:
        """This method is called just after the generate_prompt is called,
            but actually before the prompt is generated.

        Args:
            prompt (PromptGenerator): The prompt generator.

        Returns:
            PromptGenerator: The prompt generator.
        """

        # non-API commands
        

        # youtube downloads
        from .youtube_download import download_youtube_video, download_youtube_audio
        prompt.add_command(
            "download_youtube_video",
            "Download a YouTube video",
            {"url": "<video url>", "output_file": "file name"},
            download_youtube_video,
        )
        prompt.add_command(
            "download_youtube_audio",
            "Download a YouTube video's audio",
            {"url": "<video url>", "output_file": "file name"},
            download_youtube_audio,
        )

        # analyzing youtube videos and audios
        from .youtube_functions import get_youtube_transcript
        prompt.add_command(
            "get_youtube_transcript",
            "Get the transcript of a YouTube video",
            {"url": "<video url>"},
            get_youtube_transcript
        )


        # If the YouTube API key is not set, return the prompt so that the plugin commands that need an API key get skipped
        if self.yt_api_key is None:
            return prompt

        # Import the necessary functions for API commands
        from .youtube_api import search_youtube
        
        # Add the commands for autogpt to the prompt
        prompt.add_command(
            "search_youtube",
            "Search YouTube for videos",
            {"query": "<search query>"},
            search_youtube
        )


        return prompt

